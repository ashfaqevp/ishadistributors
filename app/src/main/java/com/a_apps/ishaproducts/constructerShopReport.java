package com.a_apps.ishaproducts;

public class constructerShopReport {


    private String LastPending;
    private String Price,Paid,Balance;



    public constructerShopReport() {

        this.LastPending=LastPending;
        this.Price=Price;
        this.Paid=Paid;
        this.Balance=Balance;

        // This is default constructor.
    }

    public String getLastPending() {

        return LastPending;
    }

    public void setLastPending(String LastPending) {

        this.LastPending = LastPending;
    }



    public String getPrice() {

        return Price;
    }

    public void setPrice(String Price) {

        this.Price = Price;
    }


    public String getPaid() {

        return Paid;
    }

    public void setPaid(String Paid) {

        this.Paid = Paid;
    }



    public String getBalance () {

        return Balance;
    }

    public void setBalance(String Balance) {

        this.Balance = Balance;
    }





}


